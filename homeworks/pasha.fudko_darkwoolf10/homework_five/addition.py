class Home(object):
    def __init__(self, wall, roof, doors, windows):
        self.wall = wall
        self.roof = roof
        self.doors = doors
        self.windows = windows

def __str__(self):
    return "wall: %s, roof: %s,doors, %s, windows: %s" % (self. wall, self.roof, self.doors, self.windows)
